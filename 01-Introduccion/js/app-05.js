/*

==================================================
===== Consideraciones en JavaScript con el ; =====
==================================================

en JavaScript el ; no es obligatorio a no ser que se
quiera terminar una línea de código.

Por ejemplo:
*/

console.log("Hola")
console.log("Mundo")

/*

== Nota ==
No se pueden colocar dos ordenes directas a Javascript una junto a la otra
así:

console.log("Hola") console.log("Mundo")

a no ser que se le ponga ; a la primer orden.

console.log("Hola"); console.log("Mundo")

aunque eso es una mala práctica.

*/